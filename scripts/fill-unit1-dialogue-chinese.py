def norm_jp(s: str) -> str:
    return re.sub(r"\s+", " ", (s or "").strip())


def patch_file_in_place() -> int:
    """仅替换会話 japanese 后的空 chinese，避免整文件 JSON 化破坏多行字符串。"""
    text = DATA.read_text(encoding="utf-8")
    n = 0
    for jp, zh in DIALOGUE_ZH.items():
        esc = re.escape(jp)
        pat = rf'("japanese": "{esc}",)\s*\n(\s*)"chinese": ""'
        repl = rf'\1\n\2"chinese": "{zh}"'
        new_text, c = re.subn(pat, repl, text)
        if c:
            text = new_text
            n += c
    DATA.write_text(text, encoding="utf-8")
    return n


def main() -> int:
    total = patch_file_in_place()
    print(f"[OK] {DATA.name}: {total} chinese slots filled")
    return 0 if total > 0 else 1


if __name__ == "__main__":
    raise SystemExit(main())
